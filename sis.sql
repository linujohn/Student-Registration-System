




CREATE TABLE `student` (
  `fname` varchar(30) NOT NULL,
  `lname` varchar(20) NOT NULL,
  `admn` varchar(20) NOT NULL,
  `ktu` varchar(20) NOT NULL,
  `bday` date NOT NULL,
  `branch` varchar(20) NOT NULL,
  `semester` varchar(20) NOT NULL,
  `batch` varchar(20) NOT NULL,
  `roll` int NOT NULL,
  PRIMARY KEY  (`ktu`)
) ;



